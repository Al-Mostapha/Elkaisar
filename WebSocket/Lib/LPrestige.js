  static async buildGainPrestige(Building) {

    if (LPrestige.BUILDING_PRESTIGE[Building["Type"]][Building["Lvl"]]) {
      return LPrestige.BUILDING_PRESTIGE[Building["Type"]][Building["Lvl"]];
    } else {
      return 100;
    }

  }

  static async armyGainPrestige(ArmyType, amount) {

    switch (ArmyType) {

      case "army_a": return amount * 1.5;

      case "army_b": return amount * 3;

      case "army_c": return amount * 19;

      case "army_d": return amount * 2;

      case "army_e": return amount * 8;

      case "army_f": return amount * 38;

      case "spies": return amount * 3;

      case "wall_a": return amount * 1.5;

      case "wall_b": return amount * 9;

      case "wall_c": return amount * 27;

    }

  }
  static async jopGainPrestige(amount) {

    return amount * 1;
  }


  static async eduGainPrestige(study, lvlTo) {

    return LPrestige.STUDING_PRESTIGE[study][lvlTo];

  }
  static async addPres(idPlayer, amount) {
    Elkaisar.DB.AUpdate("prestige = prestige + ?", "player", "id_player = ?", [amount, idPlayer]);
  }
